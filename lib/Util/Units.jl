# Natural/SI units conversion table
# ---------------------------------

# Energy
const GeV = 1
const MeV = GeV // 1000
const keV = MeV // 1000
const eV  = keV // 1000
const meV = eV  // 1000
const TeV = 1000GeV

# Length
const m = 1 / 1.9732705e-7eV
const cm = 1e-2m
const mm = 1e-3m
const μm = 1e-6m
const nm = 1e-9m
const pm = 1e-12m
const fm = 1e-15m
const km = 1000m

# Time
const s = 1 / 6.5821220e-16eV
const ms = 1e-3s
const μs = 1e-6s
const ns = 1e-9s
const ps = 1e-12s
const fs = 1e-15s
